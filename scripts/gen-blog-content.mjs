/**
 * Pre-build script: reads MDX files from content/blog/, compiles them to
 * HTML strings, and generates data/blog/content.generated.ts.
 *
 * Run: node scripts/gen-blog-content.mjs
 */
import fs from 'fs';
import path from 'path';
import { compile, run } from '@mdx-js/mdx';
import remarkGfm from 'remark-gfm';
import * as runtime from 'react/jsx-runtime';
import { renderToStaticMarkup } from 'react-dom/server';

const BLOG_DIR = path.join(process.cwd(), 'content/blog');
const OUT_FILE = path.join(process.cwd(), 'data/blog/content.generated.ts');

const slugs = fs
  .readdirSync(BLOG_DIR, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name);

const entries = [];

for (const slug of slugs) {
  const slugDir = path.join(BLOG_DIR, slug);
  const files = fs.readdirSync(slugDir).filter((f) => f.endsWith('.mdx'));

  for (const file of files) {
    const locale = file.replace('.mdx', '');
    const raw = fs.readFileSync(path.join(slugDir, file), 'utf-8');
    // Strip frontmatter (between --- markers)
    const body = raw.replace(/^---[\s\S]*?---\s*/, '');

    // Compile MDX to HTML at build time
    const compiled = await compile(body, { outputFormat: 'function-body', remarkPlugins: [remarkGfm] });
    const { default: MDXContent } = await run(String(compiled), {
      ...runtime,
      baseUrl: import.meta.url,
    });
    const html = renderToStaticMarkup(MDXContent({}));

    entries.push({ slug, locale, html });
  }
}

let output = `// AUTO-GENERATED by scripts/gen-blog-content.mjs — do not edit manually\n\n`;
output += `export const blogHtmlContent: Record<string, string> = {\n`;

for (const { slug, locale, html } of entries) {
  const key = `${slug}/${locale}`;
  // Escape backticks and ${} in template literal
  const escaped = html.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
  output += `  '${key}': \`${escaped}\`,\n`;
}

output += `};\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, output, 'utf-8');

console.log(`Generated ${OUT_FILE} with ${entries.length} pre-compiled HTML entries`);
