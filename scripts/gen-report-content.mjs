/**
 * Pre-build script: reads markdown reports from docs/ and compiles them to
 * HTML strings stored in data/reports/content.generated.ts.
 *
 * Run: node scripts/gen-report-content.mjs
 */
import fs from 'fs';
import path from 'path';
import { compile, run } from '@mdx-js/mdx';
import remarkGfm from 'remark-gfm';
import * as runtime from 'react/jsx-runtime';
import { renderToStaticMarkup } from 'react-dom/server';

const DOCS_DIR = path.join(process.cwd(), 'docs');
const OUT_FILE = path.join(process.cwd(), 'data/reports/content.generated.ts');

// Reports to process: { slug, file (relative to docs/) }
const reports = [
  {
    slug: 'competitive-landscape',
    file: 'COMPETITIVE_LANDSCAPE_TUBELESS_VALVES_ZH-TW.md',
  },
];

const entries = [];

for (const report of reports) {
  const filePath = path.join(DOCS_DIR, report.file);
  if (!fs.existsSync(filePath)) {
    console.warn(`Skipping ${report.file} — file not found`);
    continue;
  }

  const raw = fs.readFileSync(filePath, 'utf-8');
  // Strip any frontmatter (between --- markers) if present
  const body = raw.replace(/^---[\s\S]*?---\s*/, '');

  // Compile markdown (MDX handles plain markdown) to HTML at build time
  const compiled = await compile(body, { outputFormat: 'function-body', remarkPlugins: [remarkGfm] });
  const { default: MDXContent } = await run(String(compiled), {
    ...runtime,
    baseUrl: import.meta.url,
  });
  const html = renderToStaticMarkup(MDXContent({}));

  // Use file mtime as lastUpdated
  const stat = fs.statSync(filePath);
  const lastUpdated = stat.mtime.toISOString().slice(0, 10);

  // Check if evidence collection timestamp exists
  const evidenceTimestampFile = path.join(process.cwd(), 'data/competitive-intel/last-collected.json');
  let evidenceLastCollected = lastUpdated; // fallback to file mtime
  if (fs.existsSync(evidenceTimestampFile)) {
    try {
      const ts = JSON.parse(fs.readFileSync(evidenceTimestampFile, 'utf-8'));
      if (ts.date) evidenceLastCollected = ts.date;
    } catch { /* ignore */ }
  }

  entries.push({ slug: report.slug, html, lastUpdated, evidenceLastCollected });
}

let output = `// AUTO-GENERATED by scripts/gen-report-content.mjs — do not edit manually\n\n`;
output += `export const reportHtmlContent: Record<string, string> = {\n`;

for (const { slug, html } of entries) {
  const escaped = html.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
  output += `  '${slug}': \`${escaped}\`,\n`;
}

output += `};\n\n`;

output += `export const reportLastUpdated: Record<string, string> = {\n`;
for (const { slug, lastUpdated } of entries) {
  output += `  '${slug}': '${lastUpdated}',\n`;
}
output += `};\n\n`;

output += `export const reportEvidenceCollected: Record<string, string> = {\n`;
for (const { slug, evidenceLastCollected } of entries) {
  output += `  '${slug}': '${evidenceLastCollected}',\n`;
}
output += `};\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, output, 'utf-8');

console.log(`Generated ${OUT_FILE} with ${entries.length} pre-compiled report(s)`);
